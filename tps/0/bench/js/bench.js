const getAverage = (array) => {
	const sum = array.reduce((prev, current) => prev + current, 0);
	return sum / array.length;
};

const warmup = (preparation, cb, iterations) => {
	for (let i = 0; i < iterations; i++) {
		cb(preparation());
	}
};

/**
 * @param benchName bench name
 * @param preparation function to run before the callback to measure. What this functions returns is then passed as an argument to the cb to measure.
 * @param cb the callback function that gets measured
 * @param wmIterations the amount of warmup iterations
 * @param iterations the amount of measure iterations
 */
export const measureExecutionTime = (benchName, preparation, cb, wmIterations, iterations) => {
	const times = [];
	console.log(`=============================`);
	console.log(`Running ${benchName}...`);
	console.log(`Running warmup iterations ${wmIterations} times`);
	warmup(preparation, cb, wmIterations);
	console.log(`Running measure iterations ${iterations} times`);
	for (let i = 0; i < iterations; i++) {
		const startMark = `bench_start#${i}`;
		const endMark = `bench_end#${i}`;
		const res = preparation();
		performance.mark(startMark);
		cb(res);
		performance.mark(endMark);
		const exTime = performance.measure(`measurement#${i}`, startMark, endMark).duration;
		times.push(exTime);
	}
	const averageEx = getAverage(times);
	console.log(`Results is: ${averageEx}ms \n`);
};
